        /**
         * Crea un visor de tablas.
         *
         * @param tablaInicial Nombre de la tabla a mostrar al abrirse
         * ("alumno", "ciclo", "modulo", "matricula" o "linea_matricula"). Puede
         * ser null para mostrar el combo vacío.
         */
        public VisorTabla(String tablaInicial) {
            super("Visor de Tablas — Base de Datos");
            construirInterfaz();
            configurarEventos();

            if (tablaInicial != null && !tablaInicial.isBlank()) {
                comboTabla.setSelectedItem(tablaInicial.toLowerCase());
                cargarTabla(tablaInicial.toLowerCase());
            }

            pack();
            setMinimumSize(new Dimension(750, 450));
            setLocationRelativeTo(null); // centrar en pantalla
            setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        }

