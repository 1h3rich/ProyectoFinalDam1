        private JLabel lblSeleccion;
        private JLabel lblContador;

        // ---- Estado ----
        /**
         * Último índice de fila seleccionado EN EL MODELO (no en la vista).
         */
        private int filaSeleccionadaModelo = -1;

        // =========================================================
