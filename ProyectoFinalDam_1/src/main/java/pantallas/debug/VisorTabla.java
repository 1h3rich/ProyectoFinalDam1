        /**
         * Devuelve el valor de la columna de la fila actualmente seleccionada.
         * Útil para que la pantalla padre recupere el código al cerrar esta
         * ventana.
         *
         * @param columna Índice de la columna (0 = código).
         * @return Valor como String, o null si no hay selección.
         */
        public String getValorSeleccionado(int columna) {
            if (filaSeleccionadaModelo == -1) {
                return null;
            }
            Object valor = ((DefaultTableModel) jTable.getModel())
                    .getValueAt(filaSeleccionadaModelo, columna);
            return valor != null ? valor.toString() : null;
        }

